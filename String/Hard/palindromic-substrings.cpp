#include <bits/stdc++.h>
using namespace std;

class Solution
{
public:
    int countSubstrings(string s)
    {
        int n = s.size();
        int count = 0;

        for (int i = 0; i < n; i++)
        {
            int left = i, right = i;

            while (left >= 0 && right < n && s[left] == s[right])
            {
                left--;
                right++;
                count++;
            }

            left = i, right = i + 1;
            while (left >= 0 && right < n && s[left] == s[right])
            {
                left--;
                right++;
                count++;
            }
        }

        return count;
    }
};